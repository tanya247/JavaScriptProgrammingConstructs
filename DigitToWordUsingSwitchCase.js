function DigitToWordUsingSwitchCase(digit){
    switch(digit){
        case 1: console.log(digit+" : One");
        break;
        case 2: console.log(digit+" : Two");
        break;
        case 3: console.log(digit+" : Three");
        break;
        case 4: console.log(digit+" : Four");
        break;
        case 5: console.log(digit+" : Five");
        break;
        case 6: console.log(digit+" : Six");
        break;
        case 7: console.log(digit+" : Seven");
        break;
        case 8: console.log(digit+" : Eight");
        break;
        case 9: console.log(digit+" : Nine");
        break;
        default: console.log("Invalid");
    }    
    
}
DigitToWordUsingSwitchCase(4);